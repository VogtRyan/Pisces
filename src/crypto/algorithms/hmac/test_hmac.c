/*
 * Run the HMAC tests and report the success rate.
 */
int main()
{
    size_t onTest;

    for (onTest = 0; onTest < sizeof(allTests) / sizeof(struct hmac_test);
         onTest++) {
        run_hmac_test(&allTests[onTest]);
    }

    TEST_CONCLUDE();
}

static void run_hmac_test(const struct hmac_test *test)
{
    struct hmac_ctx *ctx;
    byte_t actual[HMAC_MAX_DIGEST_BYTES];

    ctx = hmac_alloc(test->hashAlg);
    memset(actual, 0, test->outputLen);

    hmac_single(ctx, test->key, test->keyLen, test->msg, test->msgLen, actual);
    TEST_ASSERT(memcmp(actual, test->output, test->outputLen) == 0);

    hmac_free_scrub(ctx);
}
