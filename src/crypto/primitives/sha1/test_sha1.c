/*
 * Test SHA-1 with a long message, with the message being added to the context
 * in multiple parts. The test adds a partial block; then, in a single
 * operation, completes the block, processes a full block, and starts a partial
 * block; then, adds additional data to complete the message.
 *
 * The input and expected result are from NIST CAVP SHA Test Vectors for
 * Hashing Byte-Oriented Messages, LongMsg, len=1304 (163 bytes).
 */
static void test_long_message_in_parts(void)
{
    const size_t QUARTER_BLOCK_SIZE = SHA1_BLOCK_BYTES / 4;
    const byte_t input[] = {
        0x7C, 0x9C, 0x67, 0x32, 0x3A, 0x1D, 0xF1, 0xAD, 0xBF, 0xE5, 0xCE, 0xB4,
        0x15, 0xEA, 0xEF, 0x01, 0x55, 0xEC, 0xE2, 0x82, 0x0F, 0x4D, 0x50, 0xC1,
        0xEC, 0x22, 0xCB, 0xA4, 0x92, 0x8A, 0xC6, 0x56, 0xC8, 0x3F, 0xE5, 0x85,
        0xDB, 0x6A, 0x78, 0xCE, 0x40, 0xBC, 0x42, 0x75, 0x7A, 0xBA, 0x7E, 0x5A,
        0x3F, 0x58, 0x24, 0x28, 0xD6, 0xCA, 0x68, 0xD0, 0xC3, 0x97, 0x83, 0x36,
        0xA6, 0xEF, 0xB7, 0x29, 0x61, 0x3E, 0x8D, 0x99, 0x79, 0x01, 0x62, 0x04,
        0xBF, 0xD9, 0x21, 0x32, 0x2F, 0xDD, 0x52, 0x22, 0x18, 0x35, 0x54, 0x44,
        0x7D, 0xE5, 0xE6, 0xE9, 0xBB, 0xE6, 0xED, 0xF7, 0x6D, 0x7B, 0x71, 0xE1,
        0x8D, 0xC2, 0xE8, 0xD6, 0xDC, 0x89, 0xB7, 0x39, 0x83, 0x64, 0xF6, 0x52,
        0xFA, 0xFC, 0x73, 0x43, 0x29, 0xAA, 0xFA, 0x3D, 0xCD, 0x45, 0xD4, 0xF3,
        0x1E, 0x38, 0x8E, 0x4F, 0xAF, 0xD7, 0xFC, 0x64, 0x95, 0xF3, 0x7C, 0xA5,
        0xCB, 0xAB, 0x7F, 0x54, 0xD5, 0x86, 0x46, 0x3D, 0xA4, 0xBF, 0xEA, 0xA3,
        0xBA, 0xE0, 0x9F, 0x7B, 0x8E, 0x92, 0x39, 0xD8, 0x32, 0xB4, 0xF0, 0xA7,
        0x33, 0xAA, 0x60, 0x9C, 0xC1, 0xF8, 0xD4};
    const byte_t expected[SHA1_DIGEST_BYTES] = {
        0xD8, 0xFD, 0x6A, 0x91, 0xEF, 0x3B, 0x6C, 0xED, 0x05, 0xB9,
        0x83, 0x58, 0xA9, 0x91, 0x07, 0xC1, 0xFA, 0xC8, 0xC8, 0x07};

    byte_t actual[SHA1_DIGEST_BYTES];
    memset(actual, 0, SHA1_DIGEST_BYTES);
    sha1_start(ctx);
    sha1_add(ctx, input, QUARTER_BLOCK_SIZE);
    sha1_add(ctx, input + QUARTER_BLOCK_SIZE,
             sizeof(input) - 2 * QUARTER_BLOCK_SIZE);
    sha1_add(ctx, input + sizeof(input) - QUARTER_BLOCK_SIZE,
             QUARTER_BLOCK_SIZE);
    sha1_end(ctx, actual);

    TEST_ASSERT(memcmp(actual, expected, sizeof(expected)) == 0);
}

/*
 * Run the SHA-1 tests and report the success rate.
 */
int main()
{
    ctx = sha1_alloc();

    test_empty();
    test_partial_block();
    test_full_block();
    test_long_message_in_parts();

    sha1_free_scrub(ctx);
    TEST_CONCLUDE();
}
