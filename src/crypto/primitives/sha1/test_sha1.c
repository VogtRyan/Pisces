#include "common/bytetype.h"
#include "crypto/primitives/sha1/sha1.h"
#include "crypto/test/framework.h"

#include <stddef.h>
#include <string.h>

TEST_PREAMBLE("SHA1");
static struct sha1_ctx *ctx;

/*
 * Test SHA-1 with the empty message.
 *
 * Expected result is from NIST CAVP SHA Test Vectors for Hashing Byte-Oriented
 * Messages, ShortMsg, len=0.
 */
static void test_empty(void)
{
    const byte_t expected[SHA1_DIGEST_BYTES] = {
        0xDA, 0x39, 0xA3, 0xEE, 0x5E, 0x6B, 0x4B, 0x0D, 0x32, 0x55,
        0xBF, 0xEF, 0x95, 0x60, 0x18, 0x90, 0xAF, 0xD8, 0x07, 0x09};

    byte_t actual[SHA1_DIGEST_BYTES];
    memset(actual, 0, SHA1_DIGEST_BYTES);
    sha1_start(ctx);
    sha1_end(ctx, actual);

    TEST_ASSERT(memcmp(actual, expected, sizeof(expected)) == 0);
}

/*
 * Test SHA-1 with a less-than-one-block input message.
 *
 * Input and expected result are from NIST CAVP SHA Test Vectors for Hashing
 * Byte-Oriented Messages, ShortMsg, len=24 (3 bytes).
 */
static void test_partial_block(void)
{
    const byte_t input[] = {0xDF, 0x4B, 0xD2};
    const byte_t expected[SHA1_DIGEST_BYTES] = {
        0xBF, 0x36, 0xED, 0x5D, 0x74, 0x72, 0x7D, 0xFD, 0x5D, 0x78,
        0x54, 0xEC, 0x6B, 0x1D, 0x49, 0x46, 0x8D, 0x8E, 0xE8, 0xAA};

    byte_t actual[SHA1_DIGEST_BYTES];
    memset(actual, 0, SHA1_DIGEST_BYTES);
    sha1_start(ctx);
    sha1_add(ctx, input, sizeof(input));
    sha1_end(ctx, actual);

    TEST_ASSERT(memcmp(actual, expected, sizeof(expected)) == 0);
}

